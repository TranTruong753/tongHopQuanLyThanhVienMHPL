    private void danhsachthietbiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_danhsachthietbiMouseClicked
         
        int row = danhsachthietbi.rowAtPoint(evt.getPoint());
        // lấy ra id khóa học từ dòng được chọn
        int maTB = Integer.parseInt(danhsachthietbi.getValueAt(row, 0).toString());
        ThietBi thietbi = thietbiBUS.layThietBiQuaID(maTB);
        setThietBiModel(thietbi);
        txtMaThietBi.setEnabled(false);
    }//GEN-LAST:event_danhsachthietbiMouseClicked

