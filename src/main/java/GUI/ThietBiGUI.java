    public void loadDanhSachThietBi() {
        DefaultTableModel model = (DefaultTableModel) danhsachthietbi.getModel();
        model.setRowCount(0); // xóa table 

        for (ThietBi thietbi : thietbiBUS.getAllThietBi()) {

            Object[] dataRow = new Object[3];
            dataRow[0] = thietbi.getMaTB();
            dataRow[1] = thietbi.getTenTB();
            dataRow[2] = thietbi.getMoTaTB();
            
            model.addRow(dataRow);
        }
        danhsachthietbi.setModel(model);
    }
    public void setThietBiModel(ThietBi thietbi) {
        txtMaThietBi.setText(thietbi.getMaTB() + "");
        txtTenThietBi.setText(thietbi.getTenTB() + "");
        txtMoTa.setText(thietbi.getMoTaTB() + "");
        
    }
    public void resetFormThietBi() {
        txtMaThietBi.setEnabled(true);
        txtMaThietBi.setText("");
        txtTenThietBi.setText("");
        txtMoTa.setText("");
        
    }
    
    public boolean kiemTraRong(){
        boolean check = true ;
        if(txtMaThietBi.getText().trim().isEmpty()){
            check = false;
        }
        if(txtTenThietBi.getText().trim().isEmpty()){
            check = false;
        }
        if(txtMoTa.getText().trim().isEmpty()){
            check = false;
        }
        return check;
    }
    public boolean kiemTraTrungId(String MaTB){
         for (ThietBi thietbi : thietbiBUS.getAllThietBi()) {
             if((thietbi.getMaTB()+"").equals(MaTB)){
                 return false;
             }
        }
         return true;
        
    }

