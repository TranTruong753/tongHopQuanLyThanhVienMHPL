    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
         
        String matb,tentb,mota;
        boolean ketqua;
        if(kiemTraRong()) {
            matb = txtMaThietBi.getText().trim();
            tentb = txtTenThietBi.getText().trim();
            mota = txtMoTa.getText().trim();
            

            if(!kiemTraTrungId(matb)){
                ketqua = thietbiBUS.updateThietBi(Integer.parseInt(matb), tentb, mota);
                if(ketqua){
                    loadDanhSachThietBi();
                    JOptionPane.showMessageDialog(this, "Sửa thành công!");
                }else {
                    JOptionPane.showMessageDialog(this, "Sửa thất bại!");
                }
            }else {
                JOptionPane.showMessageDialog(this, "Mã Thiết bị không tồn tại!");
            }

        }else {
            JOptionPane.showMessageDialog(this, "Ô dữ liệu bị rỗng hoặc không hợp lệ!");
        }
    }//GEN-LAST:event_jButton9ActionPerformed

