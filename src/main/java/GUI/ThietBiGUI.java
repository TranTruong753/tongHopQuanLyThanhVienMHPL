    private void btnXoaThanhVien2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaThanhVien2ActionPerformed
        // TODO add your handling code here:
        if (!txtMaThietBi.getText().isEmpty()) {
            int matb = Integer.parseInt(txtMaThietBi.getText());
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa thiết bị ?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                if (thietbiBUS.xoaThietBiTheoID(matb)) {
                    JOptionPane.showMessageDialog(this, "Xóa thiết bị thành công");
                    loadDanhSachThietBi();
                    resetFormThietBi();
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thiết bị thất bại");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn thiết bị cần xóa");
        }
    }//GEN-LAST:event_btnXoaThanhVien2ActionPerformed

