    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        try {
            String maTVText = TexMaTV.getText().trim();
            if (maTVText.equals("")) {
                JOptionPane.showMessageDialog(this, "Mã Thành Viên không được để trống", "Lỗi", JOptionPane.ERROR_MESSAGE);
                TexMaTV.setText("");
            } else {
                if (!maTVText.matches("\\d+")) {
                    JOptionPane.showMessageDialog(this, "Mã Thành Viên chỉ chứa số", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    TexMaTV.setText("");
                    return;
                }
                long maTV = Long.parseLong(maTVText);
                ThongTinSD ttsd = new ThongTinSD();
                List<ThongTinSD> listMaTT=thongTinSdBUS.loadthongtinsdMaTT();
                ttsd.setMaTT(listMaTT.size() + 1);
                ThanhVien tvInput = new ThanhVien();
                tvInput.setMaTV((int) maTV);
                LocalDateTime ldt = LocalDateTime.now();
                String strdate = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss").format(ldt);
                Timestamp timestamp = Timestamp.valueOf(strdate);
                ttsd.setTGVao(timestamp);
                ThanhVien tvResult = thanhVienBUS.CheckThanhVienVaoKhuHocTap((int) maTV);
                //                System.out.println("tvInput.getMaTV(): "+tvInput.getMaTV());
                if (tvResult != null) {
                    if (!thanhVienBUS.checkViPham((int) maTV)) {
                        ttsd.setMaTV(tvResult);
                        if (thongTinSdBUS.addthongtinsd(ttsd)) {
                            JOptionPane.showMessageDialog(this, "Thêm Thành Công", "Thành công", JOptionPane.DEFAULT_OPTION);
                            loadThongTinSDTable();
                            TexMaTV.setText("");
                        } else {
                            JOptionPane.showMessageDialog(this, "Thêm Không Thành Công", "Lỗi", JOptionPane.ERROR_MESSAGE);
                            TexMaTV.setText("");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Thành Viên Vi Phạm", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        TexMaTV.setText("");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Không Phải Thành Viên", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    TexMaTV.setText("");
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

